package domain;

public class Resurs {
	protected long idResursa;
	protected String nazivResursa;
	protected String opisResursa;
	protected String nickNameResursa;
	public long getIdResursa() {
		return idResursa;
	}
	public void setIdResursa(long idResursa) {
		this.idResursa = idResursa;
	}
	public String getNazivResursa() {
		return nazivResursa;
	}
	public void setNazivResursa(String nazivResursa) {
		this.nazivResursa = nazivResursa;
	}
	public String getOpisResursa() {
		return opisResursa;
	}
	public void setOpisResursa(String opisResursa) {
		this.opisResursa = opisResursa;
	}
	public String getNickNameResursa() {
		return nickNameResursa;
	}
	public void setNickNameResursa(String nickNameResursa) {
		this.nickNameResursa = nickNameResursa;
	}
}
